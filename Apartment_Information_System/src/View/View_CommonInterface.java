    private void choosePanel(String choice) {
        spotablepanel.setVisible(false);
        requestpanel.setVisible(false);
        transiction.setVisible(false);
        managerpanel.setVisible(false);
        residentpanel.setVisible(false);
        securitypanel.setVisible(false);

        signindesLabel.setText(choice);

        switch (choice) {
            case "Manager":
                managerpanel.setVisible(true);
                apartmentLabel.setVisible(false);
                apartmentshowLabel.setVisible(false);
                break;
            case "Resident":
                residentpanel.setVisible(true);
                break;
            case "SecurityGuard":
                securitypanel.setVisible(true);
                apartmentLabel.setVisible(false);
                apartmentshowLabel.setVisible(false);
                break;
            default:
                break;
        }
    }

